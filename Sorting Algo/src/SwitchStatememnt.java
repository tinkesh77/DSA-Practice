public class SwitchStatememnt {
    public static void main(String[] args) {
        String s = "six";
        switch (s){
            case "one" :
                System.out.println("1");
                break;
            case "two" :
                System.out.println("2");
                break;
            case "three" :
                System.out.println("3");
                break;
            case "four" :
                System.out.println("4");
                break;
            case "five" :
                System.out.println("5");
                break;
            case "six" :
                System.out.println("6");
                break;
            default:
                System.out.println("This Convert Stirgn TO int form");
        }
    }
}
